#deklarasi variabel a dan b
a,b = 10,20

hasil = a > b # value a lebih besar dari value b
print("hasil : ",a,">",b,"=",hasil) #false

hasil = a < b # value a lebih kecil dari value b
print("hasil : ",a,"<",b,"=",hasil) #true

hasil = a == b # value a sama dengan value b
print("hasil : ",a,"==",b,"=",hasil) #false

hasil = a != b # value a tidak sama dengan value b
print("hasil : ",a,"!=",b,"=",hasil) #true

hasil = a >= b # value a lebih besar sama dengan value b
print("hasil : ",a,">=",b,"=",hasil) #false

hasil = a <= b # value a lebih kecil sama dengan value b
print("hasil : ",a,"<=",b,"=",hasil) #true